package FirstExam;

import java.util.Arrays;

public class App32 {
    public static void main(String[] args) {
        int[][] a = {{1, 7, 8, 7, 5},
                    {7, 8, 5, 9, 4},
                    {5, 10, 10, 10, 10},
                    {7, 7, 7, 7, 7},
                    {5, 10, 10, 10, 10},
                    {1, 7, 8, 7, 5},
                    {4, 4, 4, 4, 4},
                    {7, 6, 8, 6, 5},
                    {5, 10, 10, 10, 10},
                    {2, 1, 3, 4, 3},
                    {10, 5, 6, 8, 6},
                    {2, 10, 9, 7, 5, 6},
                    {4, 4, 4, 4, 4},
                    {10, 10, 10, 10, 10},
                    {10, 9, 10, 9, 8, 10},
                    {5, 4, 1, 2, 9},
                    {5, 8, 6, 7, 10},
                    {5, 7, 9, 3, 10},
                    {7, 7, 7, 8, 6},
                    {10, 9, 8, 7, 6}};
        int[] b = new int[20];
        for (int i = 0; i < 20; i++) {
            b[i] = 0;
            for (int j = 0; j < 5; j++) {
                b[i] += a[i][j];
            }
            b[i] /= 5;
        }
        System.out.print(Arrays.toString(b));
    }
}
